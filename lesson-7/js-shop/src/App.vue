<template>
  <div id="nav">
    <router-view />
    {{ showcase }}
  </div>
</template>

<script>
export default {
  data() {
    return {
      showcase: null,
    };
  },
  mounted() {
    fetch("http://localhost:3000/api/v1/showcase")
      .then((res) => res.json())
      .then((data) => {
        this.showcase = data;
      });
  },
};
</script>

